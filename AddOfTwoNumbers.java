//Program to perform Arithmetic Operations
public class AddOfTwoNumbers {
	
	public static void main (String[] args) {
		int a, b, c;
		a = 10;
		b = 30;
		c = a + b;
		System.out.println("Addition of A and B is " +c);
		c = a - b;
		System.out.println("Subtraction of A and B is " +c);
	}
}

